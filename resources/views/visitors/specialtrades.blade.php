           <h3>Decentralized Finance</h3>
        
           <p>
DeFi is short for “decentralized finance,” an umbrella term for a variety of financial applications in cryptocurrency or blockchain geared toward disrupting financial intermediaries.
</p>

                             <img src="assets/images/defi.jpg" alt="icon" style="margin:25px; text-align:center;">
<p>
DeFi draws inspiration from blockchain, the technology behind the digital currency bitcoin, which allows several entities to hold a copy of a history of transactions, meaning it isn’t controlled by a single, central source. That’s important because centralized systems and human gatekeepers can limit the speed and sophistication of transactions while offering users less direct control over their money. DeFi is distinct because it expands the use of blockchain from simple value transfer to more complex financial use cases.
<p>
    <p><strong>Ethereum applications</strong><br>
Most applications that call themselves “DeFi” are built on top of Ethereum, the world’s second-largest cryptocurrency platform, which sets itself apart from the Bitcoin platform in that it’s easier to use to build other types of decentralized applications beyond simple transactions. These more complex financial use cases were even highlighted by Ethereum creator Vitalik Buterin back in 2013 in the original Ethereum white paper.
<br>
That’s because of Ethereum’s platform for smart contracts – which automatically execute transactions if certain conditions are met – offers much more flexibility. Ethereum programming languages, such as Solidity, are specifically designed for creating and deploying such smart contracts.
<br>
For example, say a user wants his or her money to be sent to a friend next Tuesday, but only if the temperature climbs above 90 degrees Fahrenheit according to weather.com. Such rules can be written in a smart contract.
<br>
With smart contracts at the core, dozens of DeFi applications are operating on Ethereum, some of which are explored below. Ethereum 2.0, a coming upgrade to Ethereum’s underlying network, could give these apps a boost by chipping away at Ethereum’s scalability issues.

</p>

<p>The most popular types of DeFi applications include:
<br><strong>
Decentralized exchanges (DEXs)</strong>: Online exchanges help users exchange currencies for other currencies, whether U.S. dollars for bitcoin or ether for DAI. DEXs are a hottype of exchange, which connects users directly so they can trade cryptocurrencies with one another without trusting an intermediary with their money.<br>
<strong>
Stablecoins</strong>: A cryptocurrency that's tied to an asset outside of cryptocurrency (the dollar or euro, for example) to stabilize the price.
<br>
<strong>Lending platforms</strong>: These platforms use smart contracts to replace intermediaries such as banks that manage lending in the middle.
<br>
<strong>"Wrapped" bitcoins (WBTC)</strong>: A way of sending bitcoin to the Ethereum network so the bitcoin can be used directly in Ethereum's DeFi system. WBTCs allow users to earn interest on the bitcoin they lend out via the decentralized lending platforms described above.
<br>
<strong>
Prediction markets</strong>: Markets for betting on the outcome of future events, such as elections. The goal of DeFi versions of prediction markets is to offer the same functionality but without intermediaries.
In addition to these apps, new DeFi concepts have sprung up around them:
<br>
<strong>
Yield farming</strong>: For knowledgeable traders who are willing to take on risk, there's yield farming, where users scan through various DeFi tokens in search of opportunities for larger returns.
<br>
<strong>
Liquidity mining</strong>: When DeFi applications entice users to their platform by giving them free tokens. This has been the buzziest form of yield farming yet.<br>
<strong>
Composability</strong>: DeFi apps are open source, meaning the code behind them is public for anyone to view. As such, these apps can be used to "compose" new apps with the code as building blocks.
<br>
<strong>
Money legos</strong>: Putting the concept "composability" another way, DeFi apps are like Legos, the toy blocks children click together to construct buildings, vehicles and so on. DeFi apps can be similarly snapped together like "money legos" to build new financial products.
</p>
