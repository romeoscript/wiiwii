                <h3>Why We Prefer a Top-Down Approach to Investing</h3>
                    <div><p>
We believe the majority of your investment returns are a result of asset allocation to your portfolio’s mix of stocks, bonds, cash, and other securities. Therefore, asset allocation likely plays a more significant role in your long-term portfolio returns than individual securities.
For example, compare an all-cash portfolio with an all-stock portfolio. Over time, you would see a much bigger difference between the all-cash and all-stock portfolios compared with two all-stock portfolios holding different stocks. The type of assets you hold plays a major role in determining the kinds of returns you receive over longer periods of time. Because of this, we believe you should first consider selecting the right asset class to invest in before analyzing individual securities.
</p>
