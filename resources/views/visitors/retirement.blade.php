                    <h3>Types of Retirement Investments: Securities</h3>
                    
                    <div><p>A security is any type of negotiable financial instrument, such as a bond, stock share, mutual fund or options contract. In fact, the word “security” has its origins in pre-digital days, when investors would receive a paper “security” certificate as proof of their investment. Though the technology that tracks them has evolved, securities remain the top vehicle people use to invest for their retirement.</p>
                    <div>
                        <strong> Stocks </strong>- Stocks represent forms of ownership in a business. Of all asset classes, stocks have historically shown the best potential returns for investors over longer time horizons, but they are subject to high volatility in the short term. Some key things to keep in mind about stocks include:
                        
                        <ul>
            
                            <li>Common stock has high growth potential, but may or may not generate income through dividends.

</li>
                            <li> Preferred stock is more likely to generate dividend income, but will generally not grow in value to the same extent as common stock.</li>
                            <li>If purchased through an account that doesn’t receive special tax considerations, the length of time a stock is held before being sold can impact how the proceeds of its sale are taxed. Stocks held for over a year in “taxable” accounts generally enjoy a lower rate, while those held for a shorter time are taxed as regular income under current law.</li>
                           
                        </ul>
                        
                        
                        <strong> Bonds -</strong>Bonds represent the debts an organization has taken on. They are, essentially, loans the investor makes to a company to be repaid at a specific time with a specific interest rate. Bonds have provided lower returns historically than stocks or similar equities, but they offer more reliable income with less short-term volatility. Key points for investors to remember with bonds include:
                        
                        <ul>
            
                            <li>Bonds generally have a fixed term called maturity. At this date, the borrower generally repays the investor’s principal and no longer has to pay interest.

</li>
                            <li>Bonds can increase or decrease in value when sold on open markets prior to maturity. For example, as interest rates move above or below the bond’s initial purchase rate, the value of the bond sold on the open market fluctuates.</li>
                            <li>As bonds mature, replacements at the same interest rate may not be available, so it is important to account for how these potential changes might affect your cash flow. This concept is called interest-rate risk.</li>
                          
                        </ul>
                        
                       
                       
                       
                        
                        <strong>  Mutual Funds - </strong>These are among the most popular retirement securities, as they allow investors who might otherwise find it impractical to purchase a diversified portfolio to pool their funds together and share the benefits. Mutual funds can be a great option for investors who are new to investing for retirement, but their fee burden (paid to the fund’s manager and salespeople) can add up significantly for larger portfolios. Despite their popularity, mutual funds do have some potential downsides investors should consider as well:
                        
                        <ul>
            
                            <li>Depending on the type of account through which the fund was purchased, tax events may be triggered even if you do nothing. Anytime securities in the fund are sold, the fund owners owe any net capital gains incurred. However, this impact can be mitigated through tax-advantaged accounts like IRAs or 401(k)s, where taxes aren’t levied until the funds are withdrawn.

</li>
                            <li>While the main benefit of mutual funds is diversification, there is a danger of having investments overly concentrated in specific sectors or products. Different funds may take a similar strategy or have significant investment overlaps, so it’s important to look at the underlying securities within each fund to maintain a diversified portfolio overall.</li>
                            <li>Similarly, exposure to multiple funds can lead investors to work against themselves. For example, if one fund you own buys a stock while another you also own sells that same stock, your portfolio doesn’t actually change but you’ll get charged fees at both ends.</li>
                            <li>
                                Funds are generally developed around a specific purpose or strategy, typically without investor feedback. As such, it’s important to regularly re-evaluate your mutual funds to consider whether they remain appropriate as your retirement plan evolves.
                            </li>
                           <li>
                               Mutual funds don’t trade in quite the same way as other securities, like stocks. Their values update at the end of each day based on the performance of the securities they own, minus any liabilities of the fund.
                               
                           </li>
                        </ul>
                        
                       
                        <strong>Exchange-Traded Funds (ETFs) - </strong>ETFs are similar products to mutual funds, but with some key differences. Like a mutual fund, they are pooled funds from multiple investors and managed by a professional money manager. However, while mutual funds can be bought and sold at a fixed price that updates daily, ETFs trade openly in the marketplace. This means ETF prices are more fluid than those of mutual funds. There are a few important considerations for investors considering ETFs:
                        
                        <ul>
            
                            <li>As with mutual funds, it’s wise to be aware of the securities held in any ETFs you own to avoid concentrating your portfolio or paying for trades on both ends.
</li>
                            <li>ETFs usually seek to mimic an index and are based on a “passive strategy,” with investors looking to hold onto the fund regardless of short-term market conditions(though ETFs taking active strategies do exist). As such, they generally have fewer trades and lower fees than traditional mutual funds. But, they can experience more volatility as their prices also move based on market perceptions, not the underlying portfolio’s value alone. Additionally, while they often try to mirror benchmarks, some funds may have large “tracking errors” relative to their actual index, so be sure to look at the specific fund’s performance history before selecting it.</li>
                            <li>Making the most of an ETF when investing for retirement requires discipline, as these funds usually attempt to mirror long-term benchmarks. Unfortunately, investors tends to become emotionally reactive to perceptions of market volatility and too often cash out without fully considering their investment time horizon.</li>
                        
                        </ul>
                        
            
